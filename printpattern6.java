package CodeToGether;

public class printpattern6 {
    public static void main(String[] args) {
        for(int i=1;i<=4;i++)
        {

            for(int j=1;j<=4;j++)
            {

                System.out.print(Math.max(i,j)+" ");

            }
            System.out.println();
        }
    }
}
